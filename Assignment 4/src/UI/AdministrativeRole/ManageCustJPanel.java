    private void rmvCustJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rmvCustJBtnActionPerformed
        // TODO add your handling code here:
       int selectedRow = custJT.getSelectedRow();
if (selectedRow < 0) {
    JOptionPane.showMessageDialog(this, "Please select a Customer.", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

int flag = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this customer?", "Warning", JOptionPane.YES_NO_OPTION);
if (flag == JOptionPane.YES_OPTION) {
    CustomerClass customer = (CustomerClass) custJT.getValueAt(selectedRow, 0);
    customerDirectory.removeCust(customer);
}

populateCustomerTable();

    }//GEN-LAST:event_rmvCustJBtnActionPerformed

