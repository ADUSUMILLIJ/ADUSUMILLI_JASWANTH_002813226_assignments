    private void populateCustomerTable()
    {
        DefaultTableModel dtm= (DefaultTableModel) custJT.getModel();
        dtm.setRowCount(0);
        
        for(CustomerClass customer:customerDirectory.getCustomerDirectory())
        {
            Object row[]= new Object[2];
            row[0]=customer;
            row[1]=customer.getCustName();
            
            dtm.addRow(row);
        }
    }

