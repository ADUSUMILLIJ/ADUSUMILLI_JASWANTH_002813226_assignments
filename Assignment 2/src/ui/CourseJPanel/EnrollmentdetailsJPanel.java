    private void populateEnrolltable() {
        DefaultTableModel dtm = (DefaultTableModel) tblCourse.getModel();
        dtm.setRowCount(0);
        for(Course user: courseList.getCourses()){
            Object[] row = new Object[6];
            
            row[0] = user;
            row[1] = user.getId();
            row[2] = user.getProfessor().getName();
            row[3] = user.getTerm();
            row[4] = user.getTime();
            row[5] = user.getProfessor().getId();
            
            dtm.addRow(row);
    }
    }
