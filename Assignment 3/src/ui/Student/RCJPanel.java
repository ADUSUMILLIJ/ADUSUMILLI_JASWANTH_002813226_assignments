    private void btndropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndropActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCourse.getSelectedRow();
        if(selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete this account ","Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Course user = (Course) tblCourse.getValueAt(selectedRow, 0);
                student.deleteCourse(user);
                populateCourseTable();
            }
        }
       
    }//GEN-LAST:event_btndropActionPerformed


