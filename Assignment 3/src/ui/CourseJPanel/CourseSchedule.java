    private void btnAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseActionPerformed
        // TODO add your handling code here:
        String name = txtCourseName.getText();
        String id = txtCourseID.getText();
        String term = boxTerm.getSelectedItem().toString();
        String time = txtTime.getText();
        String pname = txtProfName.getText();
        String pid = txtProfID.getText();
        int credit = Integer.parseInt(txtCredit.getText());
        
        Course c1 = new Course(id, name, pname, pid, term, time, credit);
        
        courseList.addCourse(c1);
        professor.addCourseToProfessor(c1);
        //pcourses.addCourse(c1);
        
      
        
        
        
        
        JOptionPane.showMessageDialog(this,"Course added");
        
        
    }//GEN-LAST:event_btnAddCourseActionPerformed

