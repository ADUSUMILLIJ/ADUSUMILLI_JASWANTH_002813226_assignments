    private void empcreatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empcreatebuttonActionPerformed
       try {
        Organization organization = (Organization) orgcmg.getSelectedItem();

        // Check if an organization is selected
        if (organization == null) {
            throw new IllegalArgumentException("Please select an organization.");
        }

        String name = txtfieldname.getText();

        // Check if the employee name is empty
        if (name.isEmpty()) {
            throw new IllegalArgumentException("Please enter the employee name.");
        }

        // Add the employee to the organization's employee repository
        organization.getEmpRepo().AddEmployee(name);

        // Update the user interface (assuming populateTable() does that)
        populateTable(organization);

        // Display a success message
        JOptionPane.showMessageDialog(this, "Employee '" + name + "' added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(null, e.getMessage(), "Invalid Input", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "An unexpected error occurred.", "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); // Print the stack trace for debugging purposes
    }
    }//GEN-LAST:event_empcreatebuttonActionPerformed

